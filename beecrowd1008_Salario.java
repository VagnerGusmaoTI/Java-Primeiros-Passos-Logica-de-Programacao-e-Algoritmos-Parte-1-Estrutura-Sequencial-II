import java.util.Locale;
import java.util.Scanner;

public class beecrowd1008_Salario {
	
public static void main(String[] args) {
		
		//ESTRUTURA SEQUENCIAL

		//Exercicio da plataforma beecrowd, nivel iniciante: 1008 Salário
		//https://www.beecrowd.com.br/judge/pt/problems/view/1008
		//OBS: ao publicar o exercício na Plataforma Beecrowd mudar o nome da classe para Main
		
		//Escreva um programa que leia o número de um funcionário, seu número de horas trabalhadas, o valor que recebe por hora e calcula o salário desse funcionário. 
		//A seguir, mostre o número e o salário do funcionário, com duas casas decimais.

		//Entrada
		//O arquivo de entrada contém 2 números inteiros e 1 número com duas casas decimais, representando o número, quantidade de horas trabalhadas e o valor que o funcionário recebe por hora trabalhada, respectivamente.

		//Saída
		//Imprima o número e o salário do funcionário, conforme exemplo fornecido, com um espaço em branco antes e depois da igualdade. No caso do salário, também deve haver um espaço em branco após o $.
		
		Locale.setDefault(Locale.US);
		Scanner sc = new Scanner(System.in);
		
		int numeroFuncional, horasTrabalhadas;
		double valorHora, salario;
		
		numeroFuncional = sc.nextInt();
		horasTrabalhadas= sc.nextInt();
		
		valorHora = sc.nextDouble();
		salario = horasTrabalhadas*valorHora;
		
		System.out.println("NUMBER = " + numeroFuncional);
		
		System.out.printf("SALARY = U$ %.2f%n", salario);
		
		
		sc.close();
	}

}
